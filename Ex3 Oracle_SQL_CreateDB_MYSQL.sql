CREATE TABLE KHACHHANG(
	MAKHACHHANG VARCHAR(10) PRIMARY KEY,
	TENCONGTY NVARCHAR(50) NOT NULL,
	TENGIAODICH NVARCHAR(50),
	DIACHI NVARCHAR(50),
	EMAIL VARCHAR(50),
	DIENTHOAI VARCHAR(15),
	FAX VARCHAR(15)
);
CREATE TABLE NHANVIEN(
	MANHANVIEN VARCHAR(10) PRIMARY KEY,
	HO NVARCHAR(10) NOT NULL,
	TEN NVARCHAR(10) NOT NULL,
	NGAYSINH DATETIME(3),
	NGAYLAMVIEC DATETIME(3),
	DIACHI NVARCHAR(50),
	DIENTHOAI VARCHAR(15),
	LUONGCOBAN DECIMAL(15,4),
	PHUCAP DECIMAL(15,4)
);
CREATE TABLE NHACUNGCAP(
	MACONGTY VARCHAR(10) PRIMARY KEY,
	TENCONGTY NVARCHAR(50) NOT NULL,
	TENGIAODICH NVARCHAR(50),
	DIACHI NVARCHAR(50),
	DIENTHOAI VARCHAR(15),
	FAX VARCHAR(15),
	EMAIL VARCHAR(50)
);
CREATE TABLE LOAIHANG(
	MALOAIHANG VARCHAR(10) PRIMARY KEY,
	TENLOAIHANG NVARCHAR(50) NOT NULL
);
CREATE TABLE MATHANG(
	MAHANG VARCHAR(10) PRIMARY KEY,
	TENHANG NVARCHAR(50) NOT NULL,
	MACONGTY VARCHAR(10),
	MALOAIHANG VARCHAR(10),
	SOLUONG INT NOT NULL,
	DONVITINH NVARCHAR(20),
	GIAHANG DECIMAL(15,4) NOT NULL,
	CONSTRAINT fkNCC_MH FOREIGN KEY  (MACONGTY) REFERENCES NHACUNGCAP(MACONGTY),
	CONSTRAINT fkNCC_LH FOREIGN KEY  (MALOAIHANG) REFERENCES LOAIHANG(MALOAIHANG)
);
CREATE TABLE DONDATHANG(
	SOHOADON INT AUTO_INCREMENT PRIMARY KEY,
	MAKHACHHANG VARCHAR(10),
	MANHANVIEN VARCHAR(10),
	NGAYDATHANG DATETIME(3),
	NGAYGIAOHANG DATETIME(3),
	NGAYCHUYENHANG DATETIME(3),
	NOIGIAOHANG NVARCHAR(50) NOT NULL ,
	CONSTRAINT fkDDH_KH FOREIGN KEY  (MAKHACHHANG) REFERENCES KHACHHANG(MAKHACHHANG),
	CONSTRAINT fkDDH_NV FOREIGN KEY  (MANHANVIEN) REFERENCES NHANVIEN(MANHANVIEN)
);
CREATE TABLE CHITIETDATHANG(
	SOHOADON INT,
	MAHANG VARCHAR(10),
	PRIMARY KEY(SOHOADON, MAHANG),
	GIABAN DECIMAL(15,4),
	SOLUONG INT,
	MUCGIAMGIA DOUBLE,
	CONSTRAINT fkCTDH_DDH FOREIGN KEY  (SOHOADON) REFERENCES DONDATHANG(SOHOADON),
	CONSTRAINT fkCTDH_NH FOREIGN KEY  (MAHANG) REFERENCES MATHANG(MAHANG)
);

